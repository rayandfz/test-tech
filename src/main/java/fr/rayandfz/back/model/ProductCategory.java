package fr.rayandfz.back.model;

/**
 * Enum representing the different categories of products available in the system.
 * This enumeration is used to categorize products and ensure consistency across
 * the application.
 *
 * <ul>
 *     <li>{@code ACCESSORIES} - Represents products that are accessories, such as jewelry, bags, and hats.</li>
 *     <li>{@code FITNESS} - Represents products related to fitness, including workout equipment, fitness trackers, and gym wear.</li>
 *     <li>{@code CLOTHING} - Represents clothing items, including shirts, pants, dresses, and outerwear.</li>
 *     <li>{@code ELECTRONICS} - Represents electronic products, including gadgets, home appliances, and personal electronics.</li>
 * </ul>
 */
public enum ProductCategory {
    ACCESSORIES,
    FITNESS,
    CLOTHING,
    ELECTRONICS
}
